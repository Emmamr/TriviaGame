var panel = $("#quiz-area");

// Question set
var questions = [{
  question: "What Italian sport car brand bares a bull in their logo?",
  answers: ["Ferrari", "Lamborghini", "Bugatti", "Peugeot"],
  correctAnswer: "Lamborghini"
}, {
  question: "The famous french city holds a 24hrs race once a year?",
  answers: ["Le Mans", "Paris", "Tokyo", "Roman"],
  correctAnswer: "Le Mans"
}, {
  question: "This car has a 7-speed dual-cluch 8.0L petrol engine?",
  answers: ["Ferrari F12", "Pagani Zonda", "Bugatti Chiron", "Tesla Model 3"],
  correctAnswer: "Bugatti Chiron"
}, {
  question: "Which car brand is known for their air cool engine during the 90's",
  answers: ["Toyota Camry", "Porsche 911", "Suburu STI", "Honda Civic"],
  correctAnswer: "Porsche 911"
}, {
  question: "Which Formula 1 driver races for Mercedes-Benz?",
  answers: ["L.Hamilton", "M.Verstappen", "S. Vettel", "F. ALonso"],
  correctAnswer: "L.Hamilton"
}, {
  question: "Which of these is part of BMW's perfomance line?",
  answers: ["C63 AMG", "GTR Skiline", "M6", "GT3 RS"],
  correctAnswer: "M6"
}];

// Variable that will hold the setInterval
var timer;

var game = {

  correct: 0,
  incorrect: 0,
  counter: 120,

  countdown: function() {
    game.counter--;
    $("#counter-number").html(game.counter);
    if (game.counter === 0) {
      console.log("TIME UP");
      game.done();
    }
  },

  start: function() {
    timer = setInterval(game.countdown, 1000);

    $("#sub-wrapper").prepend("<h2>Time Remaining: <span id='counter-number'>120</span> Seconds</h2>");

    $("#start").remove();

    for (var i = 0; i < questions.length; i++) {
      panel.append("<h2>" + questions[i].question + "</h2>");
      for (var j = 0; j < questions[i].answers.length; j++) {
        panel.append("<input type='radio' name='question-" + i +
        "' value='" + questions[i].answers[j] + "''>" + questions[i].answers[j]);
      }
    }

    panel.append("<button id='done'>Done</button>");
  },

  done: function() {

    $.each($("input[name='question-0']:checked"), function() {
      if ($(this).val() === questions[0].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-1']:checked"), function() {
      if ($(this).val() === questions[1].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-2']:checked"), function() {
      if ($(this).val() === questions[2].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-3']:checked"), function() {
      if ($(this).val() === questions[3].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-4']:checked"), function() {
      if ($(this).val() === questions[4].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-5']:checked"), function() {
      if ($(this).val() === questions[5].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-6']:checked"), function() {
      if ($(this).val() === questions[6].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    $.each($("input[name='question-7']:checked"), function() {
      if ($(this).val() === questions[7].correctAnswer) {
        game.correct++;
      }
      else {
        game.incorrect++;
      }
    });

    this.result();

  },

  result: function() {

    clearInterval(timer);

    $("#sub-wrapper h2").remove();

    panel.html("<h2>All Done!</h2>");
    panel.append("<h3>Correct Answers: " + this.correct + "</h3>");
    panel.append("<h3>Incorrect Answers: " + this.incorrect + "</h3>");
    panel.append("<h3>Unanswered: " + (questions.length - (this.incorrect + this.correct)) + "</h3>");
  }
};

// CLICK EVENTS

$(document).on("click", "#start", function() {
  game.start();
});


$(document).on("click", "#done", function() {
  game.done();
});